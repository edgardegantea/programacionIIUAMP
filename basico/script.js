// Asignación de valor a variable opcion mediante prompt

// prompt para ingresar datos
var opcion = prompt("Ingrese el valor de la opción: ");

if (opcion == 1) {
	// alert para imprimir
	alert("La opción elegida es 1");
} else if (opcion == 2) {
	alert("La opción elegida es 2");
} else {
	alert("La opción elegida es 3");
}

console.log("Hola mundo :)");

// confirm para mostrar ventana de opciones: ok, cancelar
